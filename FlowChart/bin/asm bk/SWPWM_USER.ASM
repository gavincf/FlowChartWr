
	

POR_MODULE_PCC_SWPWM EQU C_TRUE
.IF(POR_MODULE_PCC_SWPWM.EQ.C_FALSE)
	JSR	SWPWM_INIT
.ENDIF;POR_MODULE_PCC_SWPWM

	
WAITKEY_CHK_PCC_SWPWM EQU C_TRUE
.IF(WAITKEY_CHK_PCC_SWPWM.EQ.C_FALSE)
	.IF ( C_SWPWM_PATT_EN .EQ. 1 )
	JMP	BK0_DET_PATT
BK0_DET_PATT_RET:
	.ENDIF ;( C_SWPWM_PATT_EN .EQ. 1 )
.ENDIF;WAITKEY_CHK_PCC_SWPWM

USER_TMGX_ISR_PCC_SWPWM EQU C_TRUE
.IF(USER_TMGX_ISR_PCC_SWPWM.EQ.C_FALSE)
	JSR	L_PWMS_SOLVE_SUB
.ENDIF;USER_TMGX_ISR_PCC_SWPWM

;=================================================
.IF ( C_SWPWM_PATT_EN .EQ. 1 )
LOAD_PWMS_PATT_END_USER:
;special setting when When PATT end
;not clear 
	JSR	L_PWMS_STOP_W_CHK

	JMP	LOAD_PWMS_PATT_END_USER_RET

BK0_DET_PATT:
	MR_DET_CHK_PATT_EN ; check pwm PATT END
	BEQ	BK0_DET_PATT_END
	MR_DET_SWPWM_PATT_EN
	BNE	BK0_DET_PATT_END
;FREE
	MR_CHK_PATT_DIS
	
	LDA	#C_SWPWM_PATT_BIT
	TSB	DP_USER_FLAG_B
;
	
BK0_DET_PATT_END:
	JMP	BK0_DET_PATT_RET
.ENDIF ;( C_SWPWM_PATT_EN .EQ. 1 )
