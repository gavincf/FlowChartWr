;0FEH:LOOP, H , L(H:L:return point)
;0FDH:TIMES,CNT, H , L (H:L:return point)
;0F9H:START LEVEL,LEVEL,X,X
;0FAH:INC LEVEL TARGET,DIV,LEVEL,ADD VALUE
