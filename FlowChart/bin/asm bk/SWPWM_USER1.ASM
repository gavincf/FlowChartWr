	DB	0FAH,5,20,1;500MS
	DB	C_MAX_LEVEL,100,0,0;01;500MS
	DB	0FCH,7,10/5,1;500MS
	DB	10/5,100,0,0;01;500MS
	DB	0FAH,7,C_MAX_LEVEL,1;500MS
	DB	0FEH,0,2;LOOP

LED_PATT4:;@5MS;Ultimate
	DB	0F9H,1,50/5,1;
	DB	0FCH,1,00,1;250MS
	DB	000H,92,00,0;500MS
