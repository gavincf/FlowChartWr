










L_STOP_EQU:
 .IF ( C_EQU0_FLAG_EN .EQ. 1 )
 MR_CHK_EQU0_SPCH1_DIS
 .ENDIF
 MR_SET_EQU0_DLYCNT CNT_STOP 
 MR_SET_SP_FG
 .IF ( C_EQU0_EVT_EN .EQ. 1 )
 MR_EQU_NRL_NEXT
 STZ EQU_EVT_EN_FG 
 STZ EQU_EVT_OV_FG 
 .ENDIF

.IF ( C_EQU0BG1_EN .EQ. 1)
 STZ EQU0BG1_DLYCNT
 STZ EQU0BG1_DLYCNT+1
.ENDIF
.IF ( C_EQU0BG2_EN .EQ. 1)
 STZ EQU0BG2_DLYCNT
 STZ EQU0BG2_DLYCNT+1
.ENDIF
 


 RTS


L_PLAY_EQU_NEXT:
 LDA R_EQU_DATA_IDX
 CMP #0FFH
 BNE L_PLAY_EQU_NEXT_S10

 JMP L_PLAY_EQU_END
L_PLAY_EQU_NEXT_S10:
.IF (  C_EQU0_EVT_EN  .EQ. 1)
 STZ EQU_EVT_EN_FG
 STZ EQU_EVT_OV_FG
.ENDIF 
 INC <R_EQU_DATA_IDX
 BRA L_PLAY_EQU_S10

L_PLAY_EQU:









.IFDEF R_EQU_NEXT
 LDA #C_EQU0_NOP_IDX
 STA R_EQU_NEXT
.ENDIF

 STZ <R_EQU_DATA_IDX 

L_PLAY_EQU_S10:
 
 M_SET_BANK T_EQU_TBL



 LDA R_EQU_NO
 BIT #C_BIT7
 BNE L_PLAY_EQU_S11
 ASL A
 TAX
 LDA T_EQU_TBL,X
 STA DP_ADDR
 
 INX
 LDA T_EQU_TBL,X
 STA DP_ADDR+1
 BRA L_PLAY_EQU_S20

L_PLAY_EQU_S11:
 ASL A
 TAX
 LDA T_EQU_TBL1,X 
 STA DP_ADDR
 
 INX
 LDA T_EQU_TBL1,X
 STA DP_ADDR+1
 BRA L_PLAY_EQU_S20
L_PLAY_EQU_S13:
 LDA R_EQU_NO
 BIT #C_BIT7
 BNE L_PLAY_EQU_S14
 ASL A
 TAX
 LDA T2_EQU_TBL,X
 STA DP_ADDR
 
 INX
 LDA T2_EQU_TBL,X
 STA DP_ADDR+1
 BRA L_PLAY_EQU_S20

L_PLAY_EQU_S14:
 ASL A
 TAX
 LDA T2_EQU_TBL1,X
 STA DP_ADDR
 
 INX
 LDA T2_EQU_TBL1,X
 STA DP_ADDR+1

L_PLAY_EQU_S20:
 LDY R_EQU_DATA_IDX
 LDA (<DP_ADDR),Y
 STA REG_HL 
 CMP #C_EQU_END
 BEQ L_PLAY_EQU_END
 CMP #C_EQU_LOOP
 BEQ L_PLAY_EQU_LOOP
 BIT #C_EQU_EVT_BIT
 BEQ L_PLAY_EQU_NRL

 MR_EQU_EVT_NEXT

 JMP L_PLAY_EQU_GET_EVT
L_PLAY_EQU_GET_EVT_RET:
 
 BRA L_PLAY_EQU_S40

L_PLAY_EQU_END: 

 JMP L_PLAY_EQU_END_USER
L_PLAY_EQU_LOOP:

 INC R_EQU_DATA_IDX
 LDY R_EQU_DATA_IDX
 LDA (<DP_ADDR),Y
 STA R_EQU_DATA_IDX
 JMP L_PLAY_EQU_S20
 
L_PLAY_EQU_ACT_NON_RET:
L_PLAY_EQU_NRL:
 MR_EQU_NRL_NEXT

.IF (  C_EQU0_EVT_EN  .EQ. 1)
 STZ EQU_EVT_EN_FG 
 STZ EQU_EVT_OV_FG
.ENDIF
 
L_PLAY_EQU_S40:
 LDA REG_HL
 AND #C_EQU_DLY_MASK_BIT
 CMP #C_EQU_DLY
 BEQ L_PLAY_EQU_DLY





 
L_PLAY_EQU_SP: 

 INC R_EQU_DATA_IDX

.IF ( C_SPCH1_NO_2B_EN .EQ. 1)
 LDY R_EQU_DATA_IDX
 LDA (<DP_ADDR),Y
 STA SPCH1_NO1
.ENDIF

 INC R_EQU_DATA_IDX
 LDY R_EQU_DATA_IDX
 LDA (<DP_ADDR),Y
 STA SPCH1_NO


 M_SET_BANK L_PLAY_SPCH1
 JSR L_PLAY_SPCH1
 MR_SET_EQU0_DLYCNT CNT_STOP 

L_PLAY_EQU_CONTINUE:



 JMP L_PLAY_EQU_ACT
L_PLAY_EQU_ACT_RET:

L_PLAY_EQU_QUIT1:
 MR_SET_EQU_FG
 LDA #1
 RTS

L_PLAY_EQU_DLY:



 INC R_EQU_DATA_IDX
 LDY R_EQU_DATA_IDX
 LDA (<DP_ADDR),Y
 STA EQU0_DLYCNT+1
 INC R_EQU_DATA_IDX
 LDY R_EQU_DATA_IDX
 LDA (<DP_ADDR),Y
 STA EQU0_DLYCNT



 BRA L_PLAY_EQU_CONTINUE







.IF ( C_EQU0_BUF_EN .EQ. 1 )
L_PLAY_EQU_BUF_NEXT:
 INC EQU0_IDX
L_PLAY_EQU_BUF:

 LDX EQU0_IDX
 LDA EQU0_BUF,X
 CMP #0FFH
 BNE L_PLAY_EQU_BUF_S10


 MR_SET_SP_FG 

 LDA #0
 RTS
 
L_PLAY_EQU_BUF_S10:
 STA SPCH1_NO
 
 M_SET_BANK L_PLAY_SPCH1
 JSR L_PLAY_SPCH1
 
 MR_SET_EQU_FG

 LDA #1
 RTS
.ENDIF

