









C_YMOV_X_START EQU 0+C_SHIFT_PLUS
C_YMOV_Y_START EQU 32+C_SHIFT_PLUS
C_YMOV_Z_START EQU 0+C_SHIFT_PLUS


C_YMOV_Y_END EQU 0+C_SHIFT_PLUS



C_YMOV_TOLERENCE EQU 8








L_YMOV_OV:
 LDA R_YMOV_OV_DLYCNT
 ORA R_YMOV_OV_DLYCNT+1
 BEQ L_YMOV_OV_S20
 INC R_YMOV_OV_DLYCNT
 BNE L_YMOV_OV_S20
 INC R_YMOV_OV_DLYCNT+1
 BNE L_YMOV_OV_S20
 
 STZ R_YMOV_CHK_STEP
 PLAY DI_96K+[100MSEC]+DI_96K

L_YMOV_OV_S20:
 RTS





CHK_YMOV:

 LDA R_1ST_AVR_CNT
 CMP #16
 BCC CHK_YMOV_EXIT_J1

 LDA R_YMOV_STEP_DLYCNT
 BEQ CHK_YMOV_S010
 INC R_YMOV_STEP_DLYCNT
 BEQ CHK_YMOV_S010


CHK_YMOV_EXIT_J1:
 
 JMP CHK_YMOV_EXIT
 
CHK_YMOV_S010:
 LDX R_GSENSOR_SAVEP
 BNE CHK_YMOV_S011
 LDX #32
CHK_YMOV_S011:
 DEX

 LDA R_SAVEX_BASE,X
 CLC
 ADC #C_SHIFT_PLUS
 STA R_XVAL
 LDA R_SAVEY_BASE,X
 CLC
 ADC #C_SHIFT_PLUS
 STA R_YVAL
 LDA R_SAVEZ_BASE,X
 CLC
 ADC #C_SHIFT_PLUS
 STA R_ZVAL


 LDA R_YMOV_CHK_STEP
 ASL A
 TAX
 JMP (CHK_YMOV_TBL,X)
CHK_YMOV_TBL:
 DW CHK_YMOV_0
 DW CHK_YMOV_1
 DW CHK_YMOV_EXIT

CHK_YMOV_0:
 M_INT_INSIDE_PLUS R_XVAL,#(C_YMOV_X_START-C_YMOV_TOLERENCE),#(C_YMOV_X_START+C_YMOV_TOLERENCE)
 BNE CHK_YMOV_0_RELEASE
 M_INT_INSIDE_PLUS R_YVAL,#(C_YMOV_Y_START-C_YMOV_TOLERENCE),#(C_YMOV_Y_START+C_YMOV_TOLERENCE)
 BNE CHK_YMOV_0_RELEASE
 M_INT_INSIDE_PLUS R_ZVAL,#(C_YMOV_Z_START-C_YMOV_TOLERENCE),#(C_YMOV_Z_START+C_YMOV_TOLERENCE)
 BNE CHK_YMOV_0_RELEASE

CHK_YMOV_0_S100:

 INC R_YMOV_CNT
 BNE CHK_YMOV_0_S200
 DEC R_YMOV_CNT
CHK_YMOV_0_S200:
 LDA R_YMOV_CNT
 CMP #10
 BNE CHK_YMOV_EXIT1

 PLAY DI_96K
 
CHK_YMOV_EXIT1:
 JMP CHK_YMOV_EXIT

CHK_YMOV_0_RELEASE:

 LDA R_YMOV_CNT
 CMP #8 
 BCC CHK_YMOV_0_RELEASE1
 STZ R_YMOV_CNT

 MR_SET_YMOV_IDX 256-3,1
 MR_SET_YMOV_OV 

 JMP CHK_YMOV_EXIT

CHK_YMOV_0_RELEASE1:
 STZ R_YMOV_CNT 
 JMP CHK_YMOV_EXIT

CHK_YMOV_1: 


 M_INT_INSIDE_PLUS R_YVAL,#(C_YMOV_Y_END-C_YMOV_TOLERENCE),#(C_YMOV_Y_END+C_YMOV_TOLERENCE)
 BNE CHK_YMOV_EXIT



 MR_SET_YMOV_OV
 
 INC R_YMOV_CNT
 LDA R_YMOV_CNT
 CMP #6
 BCC CHK_YMOV_EXIT
 STZ R_YMOV_CNT

 M_SET_KEYCH0_FLAG
 LDA #1
 STA WB_KEYCH_CODE
 
 MR_SET_YMOV_IDX 0,0
 
CHK_YMOV_EXIT:
 RTS
